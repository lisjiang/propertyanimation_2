public class MyPoint {
    private float x;
    private float y;

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public MyPoint(float x, float y) {
        this.x = x;
        this.y = y;
    }
}
